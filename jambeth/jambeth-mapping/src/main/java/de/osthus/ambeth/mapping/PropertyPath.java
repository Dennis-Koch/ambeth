public class PropertyPath
{
	protected String propetyPath;

	public PropertyPath(String propetyPath)
	{
		super();
		this.propetyPath = propetyPath;
	}

	public String getPropetyPath()
	{
		return propetyPath;
	}

	public void setPropetyPath(String propetyPath)
	{
		this.propetyPath = propetyPath;
	}
}
